#include "ft_printf.h"

size_t	ft_strlen(const char *s)
{
	int i;
	char *cp;

	i = 0;
	if (s != NULL)
	{
		cp = (char*)s;
		while (cp[i])
			i++;
		return (i);
	}
	return (i);
}
